// criar uma funcao que vai recber 2 paramentros -> console.log para somar os dois paramentros(+)
// funcao 2 paramentros -> console.log para subtrair os dois paramentros(-)

// ...(3, 4)
// ...(31,98)

// ... (11,7)
// ... (19,52)

function somar(a, b) {
    console.log(a + b);
}

somar(3, 4);
